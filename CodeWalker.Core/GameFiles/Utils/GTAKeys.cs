            return ((A[lidx] >> bidx) & 0x1) != 0;
        }

        public void SetA(int idx)
        {
            int lidx = idx / 64;
            int bidx = idx % 64;
            A[lidx] |= (ulong)1 << bidx;
        }

        public bool GetB()
        {
            return B;
        }

        public void SetB()
        {
            this.B = true;
        }

        public static RandomGaussRow operator ^(RandomGaussRow r1, RandomGaussRow r2)
        {
            var newRow = new RandomGaussRow();
            for (int i = 0; i < 16; i++)
                newRow.A[i] = r1.A[i] ^ r2.A[i];
            newRow.B = r1.B ^ r2.B;
            return newRow;
        }
    }

