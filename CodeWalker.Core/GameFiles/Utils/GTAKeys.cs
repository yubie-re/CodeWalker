    public class GTA5Hash
    {
        public static byte[] LUT;

        static GTA5Hash()
        {
            // setup constants...
            LUT = GTA5Keys.PC_LUT;
        }

        public static uint CalculateHash(string text)
        {
            uint hash = 0;

            foreach (char c in text)
            {
                hash += (uint)LUT[c];
                hash += (hash << 10);
                hash ^= (hash >> 6);
            }

            hash += (hash << 3);
            hash ^= (hash >> 11);
            hash += (hash << 15);

            return hash;
        }
